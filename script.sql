CREATE PROCEDURE incluir_categoria
(p_ID IN NUMBER, p_NOME IN VARCHAR2)
IS
BEGIN
    INSERT INTO CATEGORIA (ID, NOME) VALUES (p_ID, UPPER(p_NOME));
    COMMIT;
END;

